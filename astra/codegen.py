from astra.ast import *


BIN_OP_MAP = {"&&": "and", "||": "or"}


def to_python(prog: Program) -> str:
    lines = ["# generated by astra", "import sys, pathlib, threading"]
    lines += [
        "_astra_heap = {}",
        "_astra_next_ptr = 1",
        "_astra_threads = {}",
        "_astra_next_tid = 1",
        "def print_(x): print(x)",
        "def len_(x): return len(x)",
        "def read_file(p): return pathlib.Path(p).read_text()",
        "def write_file(p,s): pathlib.Path(p).write_text(str(s)); return 0",
        "def args(): return sys.argv",
        "def arg(i): return sys.argv[i] if i < len(sys.argv) else ''",
        "def alloc(n):",
        "    global _astra_next_ptr",
        "    ptr = _astra_next_ptr",
        "    _astra_next_ptr += 1",
        "    _astra_heap[ptr] = bytearray(max(0, int(n)))",
        "    return ptr",
        "def free(ptr):",
        "    _astra_heap.pop(ptr, None)",
        "    return 0",
        "def spawn(fn, *a):",
        "    global _astra_next_tid",
        "    tid = _astra_next_tid",
        "    _astra_next_tid += 1",
        "    state = {'done': False, 'result': 0}",
        "    def _run():",
        "        try:",
        "            state['result'] = fn(*a)",
        "        finally:",
        "            state['done'] = True",
        "    th = threading.Thread(target=_run, daemon=True)",
        "    state['thread'] = th",
        "    _astra_threads[tid] = state",
        "    th.start()",
        "    return tid",
        "def join(tid):",
        "    state = _astra_threads.get(tid)",
        "    if state is None:",
        "        return 0",
        "    state['thread'].join()",
        "    return state['result']",
    ]
    for item in prog.items:
        if isinstance(item, StructDecl):
            fields = [n for n, _ in item.fields]
            lines.append(f"class {item.name}:")
            args = ", ".join(fields)
            lines.append(f"    def __init__(self, {args}):" if args else "    def __init__(self):")
            if fields:
                for f in fields:
                    lines.append(f"        self.{f} = {f}")
            else:
                lines.append("        pass")
            lines.append("")
            continue
        if not isinstance(item, FnDecl):
            continue
        params = ", ".join(n for n, _ in item.params)
        lines.append(f"def {item.name}({params}):")
        if not item.body:
            lines.append("    pass")
        for st in item.body:
            lines.extend(_stmt_py(st, 1))
    lines.append("if __name__ == '__main__':")
    lines.append("    rc = main()")
    lines.append("    raise SystemExit(rc if isinstance(rc, int) else 0)")
    return "\n".join(lines) + "\n"


def to_x86_64(prog: Program) -> str:
    return """global _start
section .text
_start:
  mov rax, 60
  mov rdi, 0
  syscall
"""


def _call_name(fn: Any) -> str:
    if isinstance(fn, Name):
        return fn.value
    if isinstance(fn, str):
        return fn
    return _expr(fn)


def _expr(e):
    if isinstance(e, BoolLit):
        return "True" if e.value else "False"
    if isinstance(e, NilLit):
        return "None"
    if isinstance(e, Literal):
        return repr(e.value)
    if isinstance(e, Name):
        return e.value
    if isinstance(e, Unary):
        if e.op == "!":
            return f"(not {_expr(e.expr)})"
        return f"({e.op}{_expr(e.expr)})"
    if isinstance(e, Binary):
        op = BIN_OP_MAP.get(e.op, e.op)
        return f"({_expr(e.left)} {op} {_expr(e.right)})"
    if isinstance(e, Call):
        name = _call_name(e.fn)
        name = {"print": "print_", "len": "len_"}.get(name, name)
        return f"{name}({', '.join(_expr(a) for a in e.args)})"
    if isinstance(e, IndexExpr):
        return f"({_expr(e.obj)})[{_expr(e.index)}]"
    if isinstance(e, FieldExpr):
        return f"({_expr(e.obj)}).{e.field}"
    if isinstance(e, ArrayLit):
        return f"[{', '.join(_expr(x) for x in e.elements)}]"
    return "None"


def _target_expr(t):
    if isinstance(t, Name):
        return t.value
    if isinstance(t, IndexExpr):
        return f"({_expr(t.obj)})[{_expr(t.index)}]"
    if isinstance(t, FieldExpr):
        return f"({_expr(t.obj)}).{t.field}"
    return _expr(t)


def _stmt_py(st, ind):
    p = "    " * ind
    if isinstance(st, LetStmt):
        return [f"{p}{st.name} = {_expr(st.expr)}"]
    if isinstance(st, AssignStmt):
        return [f"{p}{_target_expr(st.target)} {st.op} {_expr(st.expr)}"]
    if isinstance(st, ReturnStmt):
        return [f"{p}return {_expr(st.expr) if st.expr else 'None'}"]
    if isinstance(st, BreakStmt):
        return [f"{p}break"]
    if isinstance(st, ContinueStmt):
        return [f"{p}continue"]
    if isinstance(st, ExprStmt):
        return [f"{p}{_expr(st.expr)}"]
    if isinstance(st, IfStmt):
        lines = [f"{p}if {_expr(st.cond)}:"]
        for s in st.then_body:
            lines.extend(_stmt_py(s, ind + 1))
        if not st.then_body:
            lines.append(f"{'    ' * (ind + 1)}pass")
        if st.else_body:
            lines.append(f"{p}else:")
            for s in st.else_body:
                lines.extend(_stmt_py(s, ind + 1))
        return lines
    if isinstance(st, WhileStmt):
        lines = [f"{p}while {_expr(st.cond)}:"]
        for s in st.body:
            lines.extend(_stmt_py(s, ind + 1))
        if not st.body:
            lines.append(f"{'    ' * (ind + 1)}pass")
        return lines
    if isinstance(st, ForStmt):
        if isinstance(st.init, LetStmt) and isinstance(st.cond, (Name, Call, Binary, Literal, ArrayLit)) and st.step is None:
            lines = [f"{p}for {st.init.name} in {_expr(st.cond)}:"]
            for s in st.body:
                lines.extend(_stmt_py(s, ind + 1))
            if not st.body:
                lines.append(f"{'    ' * (ind + 1)}pass")
            return lines
        lines = []
        if st.init:
            if isinstance(st.init, LetStmt):
                lines.extend(_stmt_py(st.init, ind))
            else:
                lines.append(f"{p}{_expr(st.init)}")
        lines.append(f"{p}while {_expr(st.cond) if st.cond else 'True'}:")
        for s in st.body:
            lines.extend(_stmt_py(s, ind + 1))
        if st.step:
            lines.append(f"{'    ' * (ind + 1)}{_expr(st.step)}")
        return lines
    return [f"{p}pass"]
